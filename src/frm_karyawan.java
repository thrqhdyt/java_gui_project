        private void refreshTable() throws SQLException {
        if (tb.getRowCount() > 0) {
            for (int i = tb.getRowCount() - 1; i > -1; i--) {
                tb.removeRow(i);
            }
        }
        res = smt.executeQuery("select * from t_karyawan order by id_karyawan");
        while (res.next()) {
            tb.addRow(new Object[]{
                res.getString("id_karyawan"),
                res.getString("nama_karyawan"),
                res.getString("no_telp")
            });
        }
    }

    private void refreshTable(String keyword) throws SQLException {
        if (tb.getRowCount() > 0) {
            for (int i = tb.getRowCount() - 1; i > -1; i--) {
                tb.removeRow(i);
            }
        }
        String sql = "SELECT * FROM t_karyawan WHERE nama_karyawan LIKE '%s'";
        res = smt.executeQuery(String.format(sql, "%" + keyword + "%"));
        while (res.next()) {
            tb.addRow(new Object[]{
                res.getString("id_karyawan"),
                res.getString("nama_karyawan"),
                res.getString("no_telp")
            });
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            jTextField1.enable();
            koneksi();
            smt = con.createStatement();

            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Periksa lagi data yang anda input!!");
            } else if (jButton1.getText() == "TAMBAH") {
                String sql = "insert into t_karyawan(id_karyawan, nama_karyawan, no_telp)"
                        + " values('" + jTextField1.getText() + "',"
                        + "'" + jTextField2.getText() + "',"
                        + "'" + jTextField3.getText() + "')";
                smt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambah");
                clear();
            }
            else if (jButton1.getText() == "SIMPAN") {
                String sql = "UPDATE t_karyawan SET nama_karyawan = '%s', no_telp = '%s' WHERE id_karyawan = '%s'";
                smt.executeUpdate(String.format(sql, jTextField2.getText(), jTextField3.getText(), selectedIdKaryawan));
                JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
                clear();
                jButton1.setText("TAMBAH");
            }

            refreshTable();
        } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Perbarui");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

