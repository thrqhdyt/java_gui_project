    private void tampilDataKaryawan() {
        tb.addColumn("ID Karyawan");
        tb.addColumn("Nama");
        tb.addColumn("No Telpon");
        jTable1.setModel(tb);

        try {
            res = smt.executeQuery("Select * from t_karyawan");
            while (res.next()) {
                tb.addRow(new Object[]{
                    res.getString("id_karyawan"),
                    res.getString("nama_karyawan"),
                    res.getString("no_telp")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }
    
