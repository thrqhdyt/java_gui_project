    private void setUpSelectOption() {
        try {
            res = smt.executeQuery("select nama_pelanggan, id_transaksi, tgl_selesai from t_pelanggan join t_transaksi on(t_pelanggan.id_pelanggan = t_transaksi.id_pelanggan)");
            while (res.next()) {
                transaksi.put(res.getString("nama_pelanggan") + " - " + res.getString("tgl_selesai"), res.getString("id_transaksi"));
                jComboBox1.addItem(res.getString("nama_pelanggan") + " - " + res.getString("tgl_selesai"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }

