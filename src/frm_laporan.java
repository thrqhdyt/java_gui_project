    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            HashMap parameter = new HashMap();
            File file = new File("src/cetak.jasper");
            JasperReport jp = (JasperReport) JRLoader.loadObject(file);
            res = smt.executeQuery(String.format("select nama_pelanggan, jenis_cucian, harga, berat, tgl_masuk, tgl_selesai, harga * berat AS total_harga from t_transaksi join t_jenis_cucian on(t_transaksi.id_jenis_cucian = t_jenis_cucian.id_jenis_cucian) join t_pelanggan on(t_transaksi.id_pelanggan = t_pelanggan.id_pelanggan) where id_transaksi = '%s'", transaksi.get(jComboBox1.getSelectedItem().toString())));
            JasperPrint jasperprint = JasperFillManager.fillReport(jp, parameter, new JRResultSetDataSource(res));
            JasperViewer.viewReport(jasperprint, false);
            JasperViewer.setDefaultLookAndFeelDecorated(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error :" + e.getMessage(), "Gagal cetak nota", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

