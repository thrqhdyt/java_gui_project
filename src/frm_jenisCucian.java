    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            jTextField1.enable();
            koneksi();
            smt = con.createStatement();

            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Periksa lagi data yang anda input!!");
            } else if (jButton1.getText() == "TAMBAH") {
                String sql = "insert into t_jenis_cucian(id_jenis_cucian, jenis_cucian, harga)"
                        + " values('" + jTextField1.getText() + "',"
                        + "'" + jTextField2.getText() + "',"
                        + Integer.parseInt(jTextField3.getText()) + ")";
                smt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambah");
                clear();
            } else if (jButton1.getText() == "SIMPAN") {
                String sql = "UPDATE t_jenis_cucian SET jenis_cucian = '%s', harga = '%d' WHERE id_jenis_cucian = '%s'";
                smt.executeUpdate(String.format(sql, jTextField2.getText(), Integer.parseInt(jTextField3.getText()), selectedIdJenis));
                JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
                clear();
                jButton1.setText("TAMBAH");
            }

            refreshTable();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data Gagal Perbarui");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

