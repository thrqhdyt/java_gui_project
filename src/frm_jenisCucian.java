    private void tampilDataJenisCucian() {
        tb.addColumn("ID Jenis");
        tb.addColumn("Jenis Cucian");
        tb.addColumn("Harga");
        jTable1.setModel(tb);

        try {
            res = smt.executeQuery("Select * from t_jenis_cucian");
            while (res.next()) {
                tb.addRow(new Object[]{
                    res.getString("id_jenis_cucian"),
                    res.getString("jenis_cucian"),
                    res.getInt("harga"),});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }

